-- OPERACIONES ARITMETICAS 
SELECT 5 + 8 AS calculo;
SELECT 5 - 8 AS calculo;
SELECT 5 * 8 AS calculo;
SELECT 5 / 8 AS calculo;

-- FUNCIONES MATEMATICAS
SELECT MOD(5,4); -- calcular residuo o modulo
SELECT CEILING(9.5); -- redondea un decimal arriba(ceiling = techo)
SELECT FLOOR(9.6);-- redondea un decimal abajo
SELECT ROUND(9.7); -- redondea al entero mas cercano
SELECT POW(5,3); -- elevar un numero a la potencia
SELECT SQRT(9); -- sacar la raiz cuadrada a un numero
